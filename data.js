function formatTitle(kind, line) {
  return `${kind.name} ${line.name}`;
}

function buildGallery(kind, line) {
  return [line.image, line.promo, ...kind.gallerySet].slice(0, 4);
}

function buildHighlights(kind, line) {
  return [
    `${line.label}: ${line.focus[0]}`,
    `${kind.benefits[1].charAt(0).toUpperCase()}${kind.benefits[1].slice(1)}`,
    `${line.focus[2].charAt(0).toUpperCase()}${line.focus[2].slice(1)}`,
  ];
}

function buildProduct(kind, line) {
  const id = `${line.slug}-${kind.slug}`;
  return {
    id,
    slug: id,
    series: line.slug,
    seriesName: line.name,
    seriesLabel: line.label,
    category: kind.category,
    title: formatTitle(kind, line),
    size: kind.size,
    price: kind.basePrice + line.priceShift,
    shortDescription: productCopyMap[line.slug][kind.slug],
    intro: `${brand.displayName} | ${formatTitle(kind, line)} — ${kind.summary}`,
    heroText: `${line.name} создан для сценария, где кожа хочет ${line.focus[0]}, ${line.focus[1]} и понятный ежедневный ритм без перегрузки.`,
    gallery: buildGallery(kind, line),
    cover: line.image,
    accentImage: line.promo,
    benefits: buildHighlights(kind, line),
    usage: kind.usage,
    inci: kind.inci,
    keyIngredients: kind.keyIngredients,
    productInfo: [
      `Объём: ${kind.size}`,
      `Серия: ${seriesPriceLabels[line.slug]}`,
      "Срок после вскрытия: 6 месяцев",
      "Хранить в сухом месте при температуре от +5°C до +25°C",
    ],
  };
}

